function solution(numbers) {
    let answer = numbers
        .slice()
        .sort((a, b) => Number(a + '' + b) >= Number(b + '' + a) ? -1 : 1)
        .join('')
        .toString()

    return answer[0] === "0" ? "0" : answer
}


